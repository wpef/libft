#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = 0;
	while (s1[i] == s2[i] && i < n)
	{
		if (s1[i] == '\0' && s2[i] != '\0')
			return (0);
		else if (s2[i] == '\0' && s1[i] != '\0')
			return (0);
		i++;
	}
	if (i == n)
		return (1);
	return (0);
}
